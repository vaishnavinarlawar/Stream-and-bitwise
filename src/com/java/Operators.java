package com.java;

public class Operators {

	public static void main(String[] args) {
		int a=4;
		int b=5;
		int c=30;
		
		boolean d=true;
		boolean e=false;
		
		int f=40;
		int g=-50;
		
		String h="vaishnavi";
		String i="Rani";
		
		System.out.println(!d);   //Not Operator
		
		System.out.println(!e);   //Not Operator
		
		System.out.println(~f);  //Negation Operator
		
		System.out.println(~g);  //Negation Operator
		
		System.out.println(a<<2);  //Left Shift Operator
		
		System.out.println(c>>2);  //Right Shift Operator
		
		System.out.println(a&b); //Bitwise Integral And Operator
		
		System.out.println(a|b); //Bitwise Integral Or Operator
		
		System.out.println(b^c); //Bitwise Integral Xor Operator
		
		System.out.println("h==i" + (h==i)); //Equal eq
		
		
       }
   }
